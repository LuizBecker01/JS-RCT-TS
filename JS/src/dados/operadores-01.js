// Operadores aritméticos

// Usando valores literais
console.log(25 + 2);
console.log(25 - 2);
console.log(25 * 2);
console.log(25 / 2);
console.log(25 % 2); 

// Usando variáveis
let v1 = 25;
let v2 = 2;

console.log(v1 + v2);
console.log(v1 - v2);
console.log(v1 * v2);
console.log(v1 / v2);

//Usando incremento 
let v3 = 10;

console.log(v3++);
console.log(++v3);
console.log(v3);

console.log(v3--);
console.log(--v3);
console.log(v3);